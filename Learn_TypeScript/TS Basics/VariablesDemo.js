//declare type and initialize
var empName1 = 'John';
console.log(empName1);
//declare only type without initialization
var empName2;
empName2 = 'Jack';
console.log(empName2);
//initialize without declaring the type
var empName3 = 'Jake';
console.log(empName3);
//declare without type and initialization
var empName4;
empName4 = 'Johan';
console.log(empName4);
/*
var vs let vs const
var : can be used anywhere within a function. If declared outside the function (global),
then anywhere can be used. var variables can be updated or redefined within same scope.
let : can be used only within a block (e.g., if-else, try-catch etc), not even in function.
let or const variables cannot be updated or redefined within same scope.
*/
//primitive data types
var a = 100;
console.log(a);
var b = 'John';
console.log(b);
var c = true;
console.log(c);
var d = 'Johan';
console.log(d);
